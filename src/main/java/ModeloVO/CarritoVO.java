package ModeloVO;

/**
 *
 * @author juand
 */
public class CarritoVO {
    private String id, usuarioId;
    
      public CarritoVO() {
    }

    public CarritoVO(String id, String usuarioId) {
        this.id = id;
        this.usuarioId = usuarioId;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUsuarioId() {
        return usuarioId;
    }

    public void setUsuarioId(String usuarioId) {
        this.usuarioId = usuarioId;
    }
      
    
}
